/** SEO slug for a video (falls back to topic-id when no article entry). */
export function videoSlug(v: SiteVideo): string {
  return videoArticles[v.id]?.slug ?? `${v.topic}-${v.id}`
}

/** Resolve a video + its article from a URL slug. */
export function getVideoBySlug(slug: string): SiteVideo | undefined {
  return videos.find((v) => videoSlug(v) === slug)
}

/** All slugs, for generateStaticParams. */
export function allVideoSlugs(): string[] {
  return videos.map((v) => videoSlug(v))
}
